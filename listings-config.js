(function buildPropertyViews() {
  const formatPrice = (record) => {
    if (record.priceText) return record.priceText;
    if (typeof record.price === "number") {
      return new Intl.NumberFormat("en-US", {
        style: "currency",
        currency: "USD",
        maximumFractionDigits: 0
      }).format(record.price);
    }
    return record.price || "Contact for Price";
  };

  const records = Object.values(window.MASTER_PROPERTIES).map((record) => ({
    ...record,
    price: formatPrice(record),
    image: record.image || (record.assetBase && record.hero ? `${record.assetBase}/${record.hero}` : "assets/media-fallback.svg")
  }));

  window.LISTINGS_DATA = records;
  window.getMasterProperty = (slug) => records.find((record) => record.id === slug) || null;

  if (window.ACTIVE_PROPERTY_SLUG) {
    window.PROPERTY_DATA = window.getMasterProperty(window.ACTIVE_PROPERTY_SLUG);
  }
})();
